package json;

public class NameSubject {

	private String code;
	private String name;

	public NameSubject() {

	}

	public String getCode() {
		return code;
	}

	public String getName() {
		return name;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public void setName(String name) {
		this.name = name;
	}
}
